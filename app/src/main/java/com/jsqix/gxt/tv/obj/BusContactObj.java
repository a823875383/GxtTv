package com.jsqix.gxt.tv.obj;

public class BusContactObj extends BaseObj {
	private String bus_price;
	private String bus_safe_price;
	private String bus_sxf_price;
	private String contactName;
	private String contactId;

	public String getBus_price() {
		return bus_price;
	}

	public void setBus_price(String bus_price) {
		this.bus_price = bus_price;
	}

	public String getBus_safe_price() {
		return bus_safe_price;
	}

	public void setBus_safe_price(String bus_safe_price) {
		this.bus_safe_price = bus_safe_price;
	}

	public String getBus_sxf_price() {
		return bus_sxf_price;
	}

	public void setBus_sxf_price(String bus_sxf_price) {
		this.bus_sxf_price = bus_sxf_price;
	}

	public String getContactName() {
		return contactName;
	}

	public void setContactName(String contactName) {
		this.contactName = contactName;
	}

	public String getContactId() {
		return contactId;
	}

	public void setContactId(String contactId) {
		this.contactId = contactId;
	}

}
